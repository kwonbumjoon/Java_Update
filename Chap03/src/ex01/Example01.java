package ex01;

public class Example01 {
	public static void main(String[] args) {
		int x = 10, y = 20;
		System.out.println("x + y = " + (x + y));
		System.out.println("x - y = " + (x - y));
		System.out.println("x * y = " + (x * y));
		System.out.println("x / y = " + (x / y));
		System.out.println("x % y = " + (x % y));
		
		System.out.println("나누기 : " + 5 / 3);
		System.out.println("나머지 : " + 5 % 3);
	}
}
